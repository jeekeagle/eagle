科学实验心态的核心是什么？是你承认自己可能是错的。

一个真正的科学家不会因为实验结果和预期不符而崩溃，他只会说：有意思，这个假设被证伪了，我们需要新的假设。普通人的问题是，他们把假设当成身份——如果我的假设错了，是不是说明我很笨？不是，说明你获得了新信息，你应该感谢现实没有让你在错误的方向上走更远。

