一个直接的后果是：完美主义者往往比行动派做了更多的工作，却产出了更少的结果。准备了两周的人觉得自己比做了一天的人更努力。但准备两周的人交出来的是一份从未被检验的计划。做了一天的人交出来的是一份被真实世界打分的初稿。前者在想象中积累，后者在反馈中进化。

